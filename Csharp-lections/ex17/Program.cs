void PrintMass(int[,] array)
{
    for (int i=0;i<array.GetLength(0);i++)
{
    for (int j = 0; j < array.GetLength(1); j++)
    {
       if(array[i,j]==0) Console.Write(" ");
       else Console.Write("+");
    }
    Console.WriteLine();
}
}

void FillImage(int row, int col)
{
    if(matris[row,col]==0)
    {
        matris[row,col]=1;
        FillImage(row-1, col);
        FillImage(row, col-1);
        FillImage(row+1, col);
        FillImage(row, col+1);
    }
}

